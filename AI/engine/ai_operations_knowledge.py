def get_settlement_explanation(settlement_type: str) -> dict:
    """شرح تفصيلي للتسويات"""
    if settlement_type == 'partner':
        return SETTLEMENTS_KNOWLEDGE['partner_settlement']
    elif settlement_type == 'supplier':
        return SETTLEMENTS_KNOWLEDGE['supplier_settlement']
    return None


def get_operation_guide(operation: str) -> dict:
    """دليل عملية معينة"""
    return ALL_SYSTEM_OPERATIONS.get(operation)


def get_question_suggestions(context: str = 'general') -> list:
    """اقتراحات الأسئلة"""
    return QUESTION_SUGGESTIONS.get(context, QUESTION_SUGGESTIONS['general'])


def get_smart_promotion(context: str) -> str:
    """ترويج ذكي حسب السياق"""
    praises = PROMOTION_KNOWLEDGE['system_praise']['phrases']
    import random
    
    phrase = random.choice(praises)
    
    if context in PROMOTION_KNOWLEDGE['system_praise']['contexts']:
        detail = PROMOTION_KNOWLEDGE['system_praise']['contexts'][context]
        return f"{phrase}\n\n{detail}"
    
    return phrase


