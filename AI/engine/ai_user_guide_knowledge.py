def search_user_guide(query: str) -> dict:
    """البحث في دليل المستخدم"""
    query_lower = query.lower()
    results = []
    
    # البحث في الأسئلة الشائعة
    for question, data in GUIDE_FAQ.items():
        if any(word in query_lower for word in question.lower().split()):
            results.append({
                'type': 'faq',
                'question': question,
                'answer': data['answer'],
                'route': data.get('route'),
                'score': 10
            })
    
    # البحث في المميزات
    for section_key, section_data in USER_GUIDE_KNOWLEDGE.items():
        section_name = section_data.get('name', '')
        if any(word in query_lower for word in section_name.lower().split()):
            results.append({
                'type': 'section',
                'section': section_key,
                'name': section_name,
                'data': section_data,
                'score': 5
            })
    
    # ترتيب حسب الصلة
    results.sort(key=lambda x: x.get('score', 0), reverse=True)
    
    return {
        'query': query,
        'results': results[:5],  # أفضل 5 نتائج
        'total': len(results)
    }


def get_section_details(section_key: str) -> dict:
    """الحصول على تفاصيل قسم معين"""
    return USER_GUIDE_KNOWLEDGE.get(section_key, None)


def get_all_faqs() -> dict:
    """جميع الأسئلة الشائعة"""
    return GUIDE_FAQ
